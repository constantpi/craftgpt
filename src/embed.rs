pub struct Embedding {
    wte: Box<[[usize; EMBED_SIZE]; VOCAB_SIZE]>,
    wpe: Box<[[usize; EMBED_SIZE]; 64]>,
}

fn sign_extend(x: usize) -> usize {
    if x >= (1 << 17) {
        x | ((1 << 18) * ((1 << 6) - 1))
    } else {
        x
    }
}

impl Embedding {
    pub fn new() -> Result<Self> {
        let mut wte: Box<[[usize; EMBED_SIZE]; VOCAB_SIZE]> = vec![[0; EMBED_SIZE]; VOCAB_SIZE]
            .into_boxed_slice()
            .try_into()
            .unwrap();
        for (i, chunk) in wte.chunks_mut(32).enumerate() {
            let mut f = File::open(format!("weights/weight_files/embedding/wte_{}.bin", i + 1))?;
            for row in chunk.iter_mut() {
                for cell in row.iter_mut() {
                    let mut buf = [0u8; 3];
                    f.read_exact(&mut buf)?;
                    let res = u32::from_le_bytes([buf[0], buf[1], buf[2], 0]) as usize;
                    *cell = sign_extend(res);
                }
            }
        }
        let mut wpe: Box<[[usize; EMBED_SIZE]; 64]> = vec![[0; EMBED_SIZE]; 64]
            .into_boxed_slice()
            .try_into()
            .unwrap();
        for (i, chunk) in wpe.chunks_mut(32).enumerate() {
            let mut f = File::open(format!("weights/weight_files/embedding/wpe_{}.bin", i + 1))?;
            for row in chunk.iter_mut() {
                for cell in row.iter_mut() {
                    let mut buf = [0u8; 3];
                    f.read_exact(&mut buf)?;
                    let res = u32::from_le_bytes([buf[0], buf[1], buf[2], 0]) as usize;
                    *cell = sign_extend(res);
                }
            }
        }

        Ok(Self { wte, wpe })
    }

    pub fn get_weights(&self, token: usize, pos: usize) -> Result<Box<[usize; EMBED_SIZE]>> {
        let weights = self
            .wte
            .get(token)
            .ok_or_else(|| color_eyre::eyre::eyre!("Token index out of bounds"))?;
        let pos_weights = self
            .wpe
            .get(pos)
            .ok_or_else(|| color_eyre::eyre::eyre!("Position index out of bounds"))?;
        weights
            .iter()
            .zip(pos_weights.iter())
            .map(|(&w, &p)| (w + p) & FIXED_POINT_MASK)
            .collect::<Vec<_>>()
            .try_into()
            .map_err(|_| color_eyre::eyre::eyre!("Failed to create output array"))
    }
}

pub struct Unembedding {
    lm_head: MatMul<EMBED_SIZE, VOCAB_SIZE, false>,
    softmax_exp: Box<[usize; 1024]>,
}

impl Unembedding {
    pub fn new() -> Result<Self> {
        let mut weights: Box<[[u8; EMBED_SIZE]; VOCAB_SIZE]> = vec![[0; EMBED_SIZE]; VOCAB_SIZE]
            .into_boxed_slice()
            .try_into()
            .unwrap();
        for i in 0..48 {
            let mut f = File::open(format!(
                "weights/weight_files/unembedding/lm_head_{}.bin",
                i + 1
            ))?;
            let cur_weights = {
                let mut buf = vec![0u8; 9600];
                f.read_exact(&mut buf)?;
                buf.into_boxed_slice()
            };
            for j in 0..20 {
                let index = 48 * j + 2 * (i % 24) + 960 * (i / 24);
                let w0 = cur_weights[EMBED_SIZE * j..EMBED_SIZE * (j + 1)]
                    .try_into()
                    .unwrap();
                let w20 = cur_weights[EMBED_SIZE * (j + 20)..EMBED_SIZE * (j + 21)]
                    .try_into()
                    .unwrap();
                if i % 2 == 0 {
                    weights[index] = w0;
                    weights[index + 1] = w20;
                } else {
                    weights[index] = w20;
                    weights[index + 1] = w0;
                }
            }
        }
        let lm_head = MatMul::new(&weights);

        let mut softmax_exp: Box<[usize; 1024]> =
            vec![0; 1024].into_boxed_slice().try_into().unwrap();
        let mut f = File::open("weights/weight_files/softmax_2.bin")?;
        for v in softmax_exp.iter_mut() {
            let mut buf = [0u8; 3];
            f.read_exact(&mut buf)?;
            *v = u32::from_le_bytes([buf[0], buf[1], buf[2], 0]) as usize;
        }

        Ok(Self {
            lm_head,
            softmax_exp,
        })
    }

    pub fn forward(&self, input: &[usize; EMBED_SIZE]) -> Box<[usize; OUTPUT_SIZE]> {
        let mut logits = self.lm_head.forward(input);
        for logit in logits.iter_mut() {
            *logit ^= 1 << (FIXED_POINT_SIZE - 1);
        }
        let biggest = logits.iter().max().unwrap_or(&0);
        let softmax_sum = logits
            .iter()
            .filter_map(|logit| {
                let power = (biggest - logit) >> 12;
                self.softmax_exp.get(power)
            })
            .sum::<usize>()
            & ((1 << 32) - 1);
        let softmax_sum_inv = if softmax_sum != 0 {
            (1 << 46) / softmax_sum
        } else {
            0
        };
        let mut output = logits
            .iter()
            .enumerate()
            .map(|(i, logit)| {
                let res = self.softmax_exp.get((biggest - logit) >> 12).unwrap_or(&0);
                let res = ((softmax_sum_inv * res) >> 23) & FIXED_POINT_MASK;
                (1 << 11) * res + i
            })
            .collect::<Vec<_>>();
        // 降順にソートして上位OUTPUT_SIZE件を返す
        output.sort_unstable_by(|a, b| b.cmp(a));
        output.truncate(OUTPUT_SIZE);
        output.into_boxed_slice().try_into().unwrap()
    }
}

