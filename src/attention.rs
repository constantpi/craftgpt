fn to_float16(value: usize, offset: usize) -> usize {
    let neg = value > FIXED_POINT_MASK / 2;
    let abs = if neg {
        (FIXED_POINT_MASK + 1 - value) & (FIXED_POINT_MASK / 2)
    } else {
        value
    };
    (0..FIXED_POINT_SIZE)
        .rev()
        .find_map(|i| {
            if (abs >> i) & 1 > 0 {
                let res = ((abs << (FIXED_POINT_SIZE - i)) >> 14) & ((1 << 10) - 1);
                Some(res + ((i + 9 - offset) << 10) + ((if neg { 1 } else { 0 }) << 15))
            } else {
                None
            }
        })
        .unwrap_or(0)
}

fn float_mult(a: usize, b: usize, shift: usize) -> usize {
    let a_neg = a >= (1 << 15);
    let b_neg = b >= (1 << 15);
    let a_abs = a & ((1 << 15) - 1);
    let b_abs = b & ((1 << 15) - 1);
    let offset = ((a >> 10) & 31) + ((b >> 10) & 31);
    let neg = a_neg ^ b_neg;
    let a = if a_abs > 0 {
        (a_abs & ((1 << 10) - 1)) + (1 << 10)
    } else {
        0
    } as u128;
    let b = if b_abs > 0 {
        (b_abs & ((1 << 10) - 1)) + (1 << 10)
    } else {
        0
    } as u128;
    let res = (((a * b) << offset) >> (56 + shift)) as usize & FIXED_POINT_MASK;
    if neg {
        (FIXED_POINT_MASK + 1 - res) & FIXED_POINT_MASK
    } else {
        res
    }
}
pub struct Attention {
    matmul_key: [MatMul<EMBED_SIZE, HEAD_SIZE, false>; HEADS],
    matmul_value: [MatMul<EMBED_SIZE, HEAD_SIZE, false>; HEADS],
    matmul_query: [MatMul<EMBED_SIZE, HEAD_SIZE, false>; HEADS],
    matmul_proj: MatMul<EMBED_SIZE, EMBED_SIZE, false>,

    softmax_exps: Box<[usize; 1024]>,

    v_cache: [Vec<[usize; HEAD_SIZE]>; HEADS],
    k_cache: [Vec<[usize; HEAD_SIZE]>; HEADS],
}

impl Attention {
    pub fn new(block_num: usize) -> Result<Self> {
        // (HEADS, HEAD_SIZE, EMBED_SIZE)の3次元配列を作る
        let mut key: Box<[[[u8; EMBED_SIZE]; HEAD_SIZE]; HEADS]> =
            vec![[[0; EMBED_SIZE]; HEAD_SIZE]; HEADS]
                .into_boxed_slice()
                .try_into()
                .unwrap();
        let mut value: Box<[[[u8; EMBED_SIZE]; HEAD_SIZE]; HEADS]> =
            vec![[[0; EMBED_SIZE]; HEAD_SIZE]; HEADS]
                .into_boxed_slice()
                .try_into()
                .unwrap();
        let mut query: Box<[[[u8; EMBED_SIZE]; HEAD_SIZE]; HEADS]> =
            vec![[[0; EMBED_SIZE]; HEAD_SIZE]; HEADS]
                .into_boxed_slice()
                .try_into()
                .unwrap();
        let mut proj: Box<[[u8; EMBED_SIZE]; EMBED_SIZE]> = vec![[0; EMBED_SIZE]; EMBED_SIZE]
            .into_boxed_slice()
            .try_into()
            .unwrap();
        for i in 0..24 {
            let cur_weights = {
                let mut cur_weights = vec![0u8; 9600];
                let path = format!(
                    "weights/weight_files/attention/att_{}.bin",
                    1 + 24 * block_num + i
                );
                let mut file = File::open(path)?;
                file.read_exact(&mut cur_weights)?;
                cur_weights
            };
            for j in 0..HEADS {
                let w0 = cur_weights[EMBED_SIZE * (3 * j)..EMBED_SIZE * (3 * j + 1)].try_into()?;
                let w1 =
                    cur_weights[EMBED_SIZE * (3 * j + 1)..EMBED_SIZE * (3 * j + 2)].try_into()?;
                let w2 =
                    cur_weights[EMBED_SIZE * (3 * j + 2)..EMBED_SIZE * (3 * j + 3)].try_into()?;
                let w20 =
                    cur_weights[EMBED_SIZE * (3 * j + 20)..EMBED_SIZE * (3 * j + 21)].try_into()?;
                let w21 =
                    cur_weights[EMBED_SIZE * (3 * j + 21)..EMBED_SIZE * (3 * j + 22)].try_into()?;
                let w22 =
                    cur_weights[EMBED_SIZE * (3 * j + 22)..EMBED_SIZE * (3 * j + 23)].try_into()?;
                if i % 2 == 0 {
                    key[j][2 * i] = w0;
                    value[j][2 * i] = w1;
                    query[j][2 * i] = w2;
                    key[j][2 * i + 1] = w20;
                    value[j][2 * i + 1] = w21;
                    query[j][2 * i + 1] = w22;
                } else {
                    key[j][2 * i + 1] = w0;
                    value[j][2 * i + 1] = w1;
                    query[j][2 * i + 1] = w2;
                    key[j][2 * i] = w20;
                    value[j][2 * i] = w21;
                    query[j][2 * i] = w22;
                }
            }
            for j in 0..HEADS {
                let w15 = cur_weights[EMBED_SIZE * (j + 15)..EMBED_SIZE * (j + 16)].try_into()?;
                let w35 = cur_weights[EMBED_SIZE * (j + 35)..EMBED_SIZE * (j + 36)].try_into()?;
                if i % 2 == 0 {
                    proj[HEAD_SIZE * j + 2 * i] = w15;
                    proj[HEAD_SIZE * j + 2 * i + 1] = w35;
                } else {
                    proj[HEAD_SIZE * j + 2 * i + 1] = w15;
                    proj[HEAD_SIZE * j + 2 * i] = w35;
                }
            }
        }
        let matmul_key = key
            .iter()
            .map(MatMul::<EMBED_SIZE, HEAD_SIZE, false>::new)
            .collect::<Vec<_>>()
            .try_into()
            .unwrap();
        let matmul_value = value
            .iter()
            .map(MatMul::<EMBED_SIZE, HEAD_SIZE, false>::new)
            .collect::<Vec<_>>()
            .try_into()
            .unwrap();
        let matmul_query = query
            .iter()
            .map(MatMul::<EMBED_SIZE, HEAD_SIZE, false>::new)
            .collect::<Vec<_>>()
            .try_into()
            .unwrap();
        let matmul_proj = MatMul::<EMBED_SIZE, EMBED_SIZE, false>::new(&proj);

        let mut softmax_exps: Box<[usize; 1024]> =
            vec![0; 1024].into_boxed_slice().try_into().unwrap();
        let path = "weights/weight_files/softmax.bin";
        let mut file = File::open(path)?;
        for i in 0..1024 {
            let mut buf = [0u8; 3];
            file.read_exact(&mut buf)?;

            softmax_exps[i] = u32::from_le_bytes([buf[0], buf[1], buf[2], 0]) as usize;
        }

        Ok(Self {
            matmul_key,
            matmul_value,
            matmul_query,
            matmul_proj,
            softmax_exps,
            v_cache: Default::default(),
            k_cache: Default::default(),
        })
    }

    pub fn forward(&mut self, input: &[usize; EMBED_SIZE]) -> Box<[usize; EMBED_SIZE]> {
        let mut proj_input: Box<[[usize; HEAD_SIZE]; HEADS]> = vec![[0; HEAD_SIZE]; HEADS]
            .into_boxed_slice()
            .try_into()
            .unwrap();

        for head in 0..HEADS {
            let keys = self.matmul_key[head].forward(input);
            let keys_float: Box<[usize; HEAD_SIZE]> = keys
                .iter()
                .map(|&k| to_float16(k, 0))
                .collect::<Vec<_>>()
                .try_into()
                .unwrap();
            self.k_cache[head].push(*keys_float);

            let values = self.matmul_value[head].forward(input);
            let values_float: Box<[usize; HEAD_SIZE]> = values
                .iter()
                .map(|&v| to_float16(v, 0))
                .collect::<Vec<_>>()
                .try_into()
                .unwrap();
            self.v_cache[head].push(*values_float);

            let queries = self.matmul_query[head].forward(input);
            let queries_float: Box<[usize; HEAD_SIZE]> = queries
                .iter()
                .map(|&q| to_float16(q, 0))
                .collect::<Vec<_>>()
                .try_into()
                .unwrap();

            // relevance = [0] * len(self.k_cache[head])
            let mut relevance: Vec<usize> = self.k_cache[head]
                .iter()
                .map(|v| {
                    queries_float.iter().enumerate().fold(0, |acc, (i, &q)| {
                        (acc + float_mult(v[i], q, 5)) & FIXED_POINT_MASK
                    })
                })
                .collect();
            let mut biggest = 0;
            for r in relevance.iter_mut() {
                let neg = *r > FIXED_POINT_MASK / 2;
                *r = if neg {
                    (FIXED_POINT_MASK + 1 - *r) & FIXED_POINT_MASK
                } else {
                    *r
                };
                *r = ((*r * ATT_CONST) >> 23) & (FIXED_POINT_MASK / 2);
                if neg {
                    *r = (FIXED_POINT_MASK + 1 - *r) & FIXED_POINT_MASK;
                }
                *r ^= 1 << (FIXED_POINT_SIZE - 1);
                if *r > biggest {
                    biggest = *r;
                }
            }

            let softmax_sum: usize = relevance
                .iter()
                .map(|&r| {
                    let power = (biggest - r) >> 10;
                    self.softmax_exps.get(power).unwrap_or(&0)
                })
                .sum();
            let softmax_sum_inv = if softmax_sum > 0 {
                (1 << 39) / (softmax_sum & FIXED_POINT_MASK)
            } else {
                0
            };

            let mut output = Box::new([0; HEAD_SIZE]);

            relevance
                .iter()
                .zip(self.v_cache[head].iter())
                .for_each(|(&r, v_head)| {
                    let res = self.softmax_exps.get((biggest - r) >> 10).unwrap_or(&0);
                    let res =
                        to_float16(((softmax_sum_inv * res) >> 17) & (FIXED_POINT_MASK / 2), 4);
                    output
                        .iter_mut()
                        .zip(v_head.iter())
                        .for_each(|(output_i, &v)| {
                            *output_i = (*output_i + float_mult(res, v, 0)) & FIXED_POINT_MASK;
                        });
                });
            proj_input[head] = *output;
        }
        self.matmul_proj
            .forward(&Box::new(proj_input.concat().try_into().unwrap()))
    }
}

