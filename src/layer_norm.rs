use crate::consts::*;

use color_eyre::eyre::Result;
use std::fs::File;
use std::io::{self, Read};

pub struct LayerNorm {
    pub weights: [usize; EMBED_SIZE],
}

impl LayerNorm {
    pub fn new(index: usize) -> Result<Self> {
        let path = format!("weights/weight_files/layernorm/ln_{}.bin", index);
        let mut f = File::open(path)?;
        let mut weights = [0usize; EMBED_SIZE];
        for i in 0..EMBED_SIZE {
            let mut buf = [0u8; 3];
            f.read_exact(&mut buf)?;
            let cur = u32::from_le_bytes([buf[0], buf[1], buf[2], 0]);
            weights[i] = (cur / 2) as usize;
        }
        Ok(Self { weights })
    }

    pub fn forward(&self, input: &[usize; EMBED_SIZE]) -> [usize; EMBED_SIZE] {
        fn sub(a: usize, b: usize) -> usize {
            let ai32 = a as i32;
            let bi32 = b as i32;
            (if ai32 >= bi32 {
                (ai32 - bi32) as usize
            } else {
                (1 << FIXED_POINT_SIZE) - ((bi32 - ai32) as usize)
            }) & FIXED_POINT_MASK
        }

        let sum = input
            .iter()
            .map(|&x| {
                if x > FIXED_POINT_MASK / 2 {
                    x + (255 << FIXED_POINT_SIZE)
                } else {
                    x
                }
            })
            .sum::<usize>()
            & ((1 << 32) - 1);
        let neg = sum >= (1 << 31);
        let abs = if neg { (1 << 32) - sum } else { sum };
        let abs_mean = (abs * LAYERNORM_CONST) >> 32;
        let mean = if neg { (1 << 32) - abs_mean } else { abs_mean };

        // let mut sigma2 = EPS;
        let sigma2 = input.iter().fold(EPS, |acc, &v| {
            let diff = sub(v, mean);
            let abs_diff = if diff > FIXED_POINT_MASK / 2 {
                (1 << FIXED_POINT_SIZE) - diff
            } else {
                diff
            };
            (acc + abs_diff * abs_diff) & ((1 << 48) - 1)
        });
        let sqrt = (sigma2 as f64).sqrt() as usize;
        let sigma = ((LAYERNORM_CONST_2 * sqrt) >> 27) & FIXED_POINT_MASK;
        let inv_sigma = ((1 << (2 * MATMUL_FIXED_POINT)) / sigma) & FIXED_POINT_MASK;
        input
            .iter()
            .enumerate()
            .map(|(i, &v)| {
                let diff = sub(v, mean);
                let neg = diff >= (FIXED_POINT_MASK / 2);
                let abs_diff = if neg {
                    (1 << FIXED_POINT_SIZE) - diff
                } else {
                    diff
                };
                let res = ((abs_diff * inv_sigma) >> MATMUL_FIXED_POINT) & (FIXED_POINT_MASK / 2);
                let weighted =
                    ((res * self.weights[i]) >> (MATMUL_FIXED_POINT + 3)) & (FIXED_POINT_MASK / 2);
                if neg {
                    (1 << FIXED_POINT_SIZE) - weighted
                } else {
                    weighted
                }
            })
            .collect::<Vec<_>>()
            .try_into()
            .unwrap()
    }
}

