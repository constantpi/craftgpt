// 中間層のサイズ
const INTERMEDIATE_SIZE: usize = EMBED_SIZE * MLP_SCALE;

#[allow(clippy::upper_case_acronyms)]
pub struct MLP {
    matmul_up: MatMul<EMBED_SIZE, INTERMEDIATE_SIZE, true>,
    matmul_down: MatMul<INTERMEDIATE_SIZE, EMBED_SIZE, false>,
}

impl MLP {
    pub fn new(block_num: usize) -> Result<Self> {
        let mut weights_up: Box<[[u8; EMBED_SIZE]; INTERMEDIATE_SIZE]> =
            vec![[0u8; EMBED_SIZE]; INTERMEDIATE_SIZE]
                .into_boxed_slice()
                .try_into()
                .unwrap();
        let mut weights_down: Box<[[u8; INTERMEDIATE_SIZE]; EMBED_SIZE]> =
            vec![[0u8; INTERMEDIATE_SIZE]; EMBED_SIZE]
                .into_boxed_slice()
                .try_into()
                .unwrap();
        for i in 0..24 {
            let path = format!(
                "weights/weight_files/mlp/mlp_{}.bin",
                25 + 48 * block_num + i
            );
            let mut f = File::open(path)?;
            let mut weights = vec![0u8; 9600];
            for weight in weights.iter_mut() {
                let mut buf = [0u8; 1];
                f.read_exact(&mut buf)?;
                *weight = buf[0];
            }
            for j in 0..5 {
                let a = weights[INTERMEDIATE_SIZE * j..INTERMEDIATE_SIZE * (j + 1)]
                    .try_into()
                    .unwrap();
                let b = weights[INTERMEDIATE_SIZE * (j + 5)..INTERMEDIATE_SIZE * (j + 6)]
                    .try_into()
                    .unwrap();
                if i % 2 == 0 {
                    weights_down[48 * j + 2 * i] = a;
                    weights_down[48 * j + 2 * i + 1] = b;
                } else {
                    weights_down[48 * j + 2 * i + 1] = a;
                    weights_down[48 * j + 2 * i] = b;
                }
            }
        }
        for i in 0..24 {
            let path = format!(
                "weights/weight_files/mlp/mlp_{}.bin",
                1 + 48 * block_num + i
            );
            let mut f = File::open(path)?;
            let mut weights = vec![0u8; 9600];
            for weight in weights.iter_mut() {
                let mut buf = [0u8; 1];
                f.read_exact(&mut buf)?;
                *weight = buf[0];
            }
            for j in 0..20 {
                let a = weights[EMBED_SIZE * j..EMBED_SIZE * (j + 1)]
                    .try_into()
                    .unwrap();
                let b = weights[EMBED_SIZE * (j + 20)..EMBED_SIZE * (j + 21)]
                    .try_into()
                    .unwrap();
                if i % 2 == 0 {
                    weights_up[48 * j + 2 * i] = a;
                    weights_up[48 * j + 2 * i + 1] = b;
                } else {
                    weights_up[48 * j + 2 * i + 1] = a;
                    weights_up[48 * j + 2 * i] = b;
                }
            }
        }
        Ok(Self {
            matmul_up: MatMul::new(&weights_up),
            matmul_down: MatMul::new(&weights_down),
        })
    }

    pub fn forward(&self, input: &[usize; EMBED_SIZE]) -> Box<[usize; EMBED_SIZE]> {
        let intermediate = self.matmul_up.forward(input);
        self.matmul_down.forward(&intermediate)
    }
}

