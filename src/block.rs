use color_eyre::eyre::Result;

use crate::attention::Attention;
use crate::layer_norm::LayerNorm;
use crate::mlp::MLP;
use crate::{consts::*, mlp};

pub struct Block {
    attention: Attention,
    mlp: MLP,
    norm1: LayerNorm,
    norm2: LayerNorm,
}

impl Block {
    pub fn new(block_num: usize) -> Result<Self> {
        let attention = Attention::new(block_num)?;
        let mlp = MLP::new(block_num)?;
        let norm1 = LayerNorm::new(2 * block_num + 1)?;
        let norm2 = LayerNorm::new(2 * block_num + 2)?;
        Ok(Self {
            attention,
            mlp,
            norm1,
            norm2,
        })
    }

    pub fn forward(&mut self, input: &[usize; EMBED_SIZE]) -> Box<[usize; EMBED_SIZE]> {
        println!("Input: {:?}", input);
        println!("norm1 result: {:?}", self.norm1.forward(input));
        let att_diff = self.attention.forward(&self.norm1.forward(&input));
        println!("att_diff: {:?}", att_diff);
        let att_out = input
            .iter()
            .zip(att_diff.iter())
            .map(|(&x, &diff)| (x + diff) & FIXED_POINT_MASK)
            .collect::<Vec<_>>()
            .try_into()
            .unwrap();
        println!("att_out: {:?}", att_out);
        let mlp_diff = self.mlp.forward(&self.norm2.forward(&att_out));
        println!("mlp_diff: {:?}", mlp_diff);
        att_out
            .iter()
            .zip(mlp_diff.iter())
            .map(|(&x, &diff)| (x + diff) & FIXED_POINT_MASK)
            .collect::<Vec<_>>()
            .try_into()
            .unwrap()
    }
}

